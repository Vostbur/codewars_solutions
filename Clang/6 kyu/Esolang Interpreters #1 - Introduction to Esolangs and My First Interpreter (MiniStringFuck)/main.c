#include <stdio.h>
#include <stdlib.h>

char *my_first_interpreter(const char *code)
{
    char command;
    char mem = 0;
    char *result, *output;

    result = output = calloc(sizeof(char), 3000);

    while (command = *code++)
    {
        switch (command)
        {
        case '+':
            mem++;
            break;
        case '.':
            *result++ = mem;
        }
    }
    *result = '\0';

    return output;
}

